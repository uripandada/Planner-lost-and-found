import { Injectable } from '@angular/core';
import { Resolve } from '@angular/router';
import { Observable } from 'rxjs';
import { ColorsManagementClient, GetRccHousekeepingColorsQuery, RccHousekeepingStatusColorDetails } from '../autogenerated-clients/api-client';

@Injectable({ providedIn: 'root' })
export class RccHousekeepingStatusColorListResolver implements Resolve<Observable<RccHousekeepingStatusColorDetails[]>> {
  constructor(private _client: ColorsManagementClient) { }

  resolve() {
    return this._client.getListOfRccHousekeepingStatusColors(new GetRccHousekeepingColorsQuery({}));
  }
}
