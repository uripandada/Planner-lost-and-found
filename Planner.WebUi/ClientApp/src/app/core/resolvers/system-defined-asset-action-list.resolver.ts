import { Injectable } from '@angular/core';
import { ActivatedRouteSnapshot, Resolve } from '@angular/router';
import { Observable } from 'rxjs';
import { AssetManagementClient, GetSystemDefinedAssetActionsQuery, SystemDefinedAssetAction } from '../autogenerated-clients/api-client';

@Injectable({ providedIn: 'root' })
export class SystemDefinedAssetActionListResolver implements Resolve<Observable<SystemDefinedAssetAction[]>> {
  constructor(private client: AssetManagementClient) { }

  resolve(routeSnapshot: ActivatedRouteSnapshot) {
    return this.client.getSystemDefinedAssetActions(new GetSystemDefinedAssetActionsQuery({}));
  }
}
