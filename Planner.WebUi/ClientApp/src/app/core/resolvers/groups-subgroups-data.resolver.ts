import { Injectable } from '@angular/core';
import { ActivatedRouteSnapshot, Resolve } from '@angular/router';
import { Observable } from 'rxjs';
import {  ProcessResponseOfFullGroupHierarchyData, UserManagementClient, UserModel } from '../autogenerated-clients/api-client';

@Injectable({ providedIn: 'root' })
export class GroupsSubGroupsDataResolver implements Resolve<Observable<ProcessResponseOfFullGroupHierarchyData>> {
  constructor(private userManagementClient: UserManagementClient) { }

  resolve(routeSnapshot: ActivatedRouteSnapshot) {
    return this.userManagementClient.getGroupsAndSubGroups();
  }
}
