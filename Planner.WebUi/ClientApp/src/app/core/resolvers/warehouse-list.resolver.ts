import { Injectable } from '@angular/core';
import { ActivatedRouteSnapshot, Resolve } from '@angular/router';
import { Observable } from 'rxjs';
import { GetListOfWarehousesQuery, WarehouseData, WarehouseManagementClient } from '../autogenerated-clients/api-client';

@Injectable({ providedIn: 'root' })
export class WarehouseListResolver implements Resolve<Observable<Array<WarehouseData>>> {
  constructor(private client: WarehouseManagementClient) { }

  resolve(routeSnapshot: ActivatedRouteSnapshot) {
    return this.client.getListOfWarehouses(new GetListOfWarehousesQuery({}));
  }
}
