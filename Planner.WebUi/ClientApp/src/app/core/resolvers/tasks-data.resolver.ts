import { Injectable } from "@angular/core";
import { ActivatedRouteSnapshot, Resolve } from "@angular/router";
import { Observable } from "rxjs";
import { GetTasksDataQuery, TasksData, TasksManagementClient } from "../autogenerated-clients/api-client";

@Injectable({ providedIn: "root" })
export class TasksDataResolver implements Resolve<Observable<TasksData>> {
  constructor(private tasksManagementClient: TasksManagementClient) { }

  resolve(routeSnapshot: ActivatedRouteSnapshot) {
    return this.tasksManagementClient.getTasksData(new GetTasksDataQuery({}));
  }
}
