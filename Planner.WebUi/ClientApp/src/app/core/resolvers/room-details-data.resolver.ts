import { Injectable } from '@angular/core';
import { ActivatedRouteSnapshot, Resolve } from '@angular/router';
import { Observable } from 'rxjs';
import { GetRoomDetailsQuery, RoomDetailsData, RoomManagementClient } from '../autogenerated-clients/api-client';

@Injectable({ providedIn: 'root' })
export class RoomDetailsDataResolver implements Resolve<Observable<RoomDetailsData>> {
  constructor(private roomManagementClient: RoomManagementClient) { }

  resolve(routeSnapshot: ActivatedRouteSnapshot) {
    const roomId: string = routeSnapshot.queryParams['roomId'];
    return this.roomManagementClient.getRoomDetails(new GetRoomDetailsQuery({ id: roomId }));
  }
}
