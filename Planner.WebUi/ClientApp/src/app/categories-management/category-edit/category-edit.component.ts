import { Component, OnInit, Input, Output, EventEmitter, OnChanges, SimpleChanges } from '@angular/core';
import { FormBuilder, FormGroup, ValidationErrors, Validators } from '@angular/forms';
import { Router } from '@angular/router';
import { ToastrService } from 'ngx-toastr';
import { BehaviorSubject } from 'rxjs';
import { InsertRoomCategoryCommand, ProcessResponse, ProcessResponseOfGuid, RoomCategoryDetailsViewModel, RoomCategoryManagementClient, UpdateRoomCategoryCommand } from '../../core/autogenerated-clients/api-client';
import { LoadingService } from '../../core/services/loading.service';

@Component({
  selector: 'app-room-category-edit',
  templateUrl: './category-edit.component.html',
  styleUrls: ['./category-edit.component.scss']
})
export class CategoryEditComponent implements OnInit, OnChanges {

  @Input() category: RoomCategoryDetailsViewModel;

  @Output() deleted: EventEmitter<string> = new EventEmitter<string>(); 
  @Output() inserted: EventEmitter<RoomCategoryDetailsViewModel> = new EventEmitter<RoomCategoryDetailsViewModel>();
  @Output() updated: EventEmitter<RoomCategoryDetailsViewModel> = new EventEmitter<RoomCategoryDetailsViewModel>();
  @Output() cancelled: EventEmitter<boolean> = new EventEmitter<boolean>();

  isSaving$: BehaviorSubject<boolean> = new BehaviorSubject<boolean>(false);
  isCreateNew$: BehaviorSubject<boolean> = new BehaviorSubject<boolean>(false);

  categoryForm: FormGroup;
  loading: LoadingService;

  constructor(private _router: Router, private _toastr: ToastrService, private _formBuilder: FormBuilder,
    private _roomCategoryManagementClient: RoomCategoryManagementClient,) {
    this.loading = new LoadingService();
  }

  ngOnInit(): void {
    this._setCreateNewStatus();

    this.categoryForm = this._formBuilder.group({
      name: [this.category.name, [Validators.required]],
      isPrivate: [this.category.isPrivate, [Validators.required]],
      isDefaultForReservationSync: [this.category.isDefaultForReservationSync, [Validators.required]],
    });
  }

  ngOnChanges(changes: SimpleChanges): void {
    if (changes.category && !changes.category.firstChange) {
      this._setCreateNewStatus(); 
      this._setCategoryFormData();
    }
  }

  cancel() {
    this.cancelled.next(true);
  }

  private _setCreateNewStatus() {
    if (this.category.id) {
      this.isCreateNew$.next(false);
    }
    else {
      this.isCreateNew$.next(true);
    }
  }

  private _setCategoryFormData() {
    this.categoryForm.controls.name.setValue(this.category.name);
    this.categoryForm.controls.isPrivate.setValue(this.category.isPrivate);

    this.categoryForm.updateValueAndValidity({ onlySelf: false });

    this.categoryForm.markAsUntouched({ onlySelf: false });
    this.categoryForm.markAsPristine({ onlySelf: false });
  }

  save() {
    if (!this.categoryForm.valid) {
      this.categoryForm.markAllAsTouched();
      this.categoryForm.markAsDirty({ onlySelf: false });
      this._toastr.error("You have to fix form errors before you can continue.");
      return;
    }

    let formValues = this.categoryForm.getRawValue();

    if (this.isCreateNew$.value) {
      this._insertRoomCategory();
    }
    else {
      this._updateRoomCategory();
    }
  }

  private _insertRoomCategory() {
    let formValues = this.categoryForm.getRawValue();
    let insertRequest: InsertRoomCategoryCommand = new InsertRoomCategoryCommand({
      isPrivate: formValues.isPrivate,
      name: formValues.name,
      isDefaultForReservationSync: formValues.isDefaultForReservationSync,
    });

    this.loading.start();

    this.isSaving$.next(true);

    this._roomCategoryManagementClient.insertRoomCategory(insertRequest).subscribe(
      (response: ProcessResponseOfGuid) => {
        if (response.hasError) {
          this._toastr.error(response.message);
          this._setFormValidationErrors(response, this.categoryForm);
          this.isSaving$.next(false);
          return;
        }

        this.isSaving$.next(false);

        let roomCategoryDetails = new RoomCategoryDetailsViewModel({
          id: response.data,
          isPrivate: insertRequest.isPrivate,
          name: insertRequest.name,
          isDefaultForReservationSync: insertRequest.isDefaultForReservationSync,
          isSystemDefaultForReservationSync: false,
        });

        this.inserted.next(roomCategoryDetails);
      },
      (error: Error) => {
        this._toastr.error(error.message);
        this.isSaving$.next(false);
      },
      () => {
        this.loading.stop();
        this.isSaving$.next(false);
      },
    );
  }
  private _updateRoomCategory() {
    let formValues = this.categoryForm.getRawValue();
    let updateRequest: UpdateRoomCategoryCommand = new UpdateRoomCategoryCommand({
      id: this.category.id,
      isPrivate: formValues.isPrivate,
      name: formValues.name,
      isDefaultForReservationSync: formValues.isDefaultForReservationSync,
    });

    this.loading.start();
    this.isSaving$.next(true);

    this._roomCategoryManagementClient.updateRoomCategory(updateRequest).subscribe(
      (response: ProcessResponse) => {
        if (response.hasError) {
          this._toastr.error(response.message);
          this._setFormValidationErrors(response, this.categoryForm);
          this.isSaving$.next(false);
          return;
        }

        this._toastr.success(response.message);
        this.isSaving$.next(false);

        let roomCategoryDetails = new RoomCategoryDetailsViewModel({
          id: updateRequest.id,
          isPrivate: updateRequest.isPrivate,
          name: updateRequest.name,
          isDefaultForReservationSync: updateRequest.isDefaultForReservationSync,
          isSystemDefaultForReservationSync: this.category.isSystemDefaultForReservationSync,
        });

        this.updated.next(roomCategoryDetails);
      },
      (error: Error) => {
        this._toastr.error(error.message);
        this.isSaving$.next(false);
      },
      () => {
        this.loading.stop();
        this.isSaving$.next(false);
      },
    );
  }
  private _setFormValidationErrors(response: ProcessResponse, form: FormGroup) {
    for (let error of response.modelErrors) {
      let control = form.get(error.key);

      if (control) {
        var errors: ValidationErrors = {};
        errors[error.validatorKey] = true;

        control.setErrors(errors)
        control.markAsDirty();
        control.markAsTouched();
      }
    }
  }
}
