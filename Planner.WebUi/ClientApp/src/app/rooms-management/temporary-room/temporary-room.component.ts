import { Component, OnInit, Input, ChangeDetectionStrategy } from '@angular/core';
import { FormArray, FormGroup } from '@angular/forms';
import { Router } from '@angular/router';
import { UnassignedRoomData } from '../../core/autogenerated-clients/api-client';

@Component({
  selector: 'app-temporary-room',
  templateUrl: './temporary-room.component.html',
  styleUrls: ['./temporary-room.component.scss'],
  changeDetection: ChangeDetectionStrategy.OnPush,
})
export class TemporaryRoomComponent implements OnInit {

  @Input() roomIndex: number;
  @Input() room: UnassignedRoomData;

  public roomCategory: string = "";
  public roomTypeDescription: string = "";
  public showBedsSection: boolean = false;

  public isAppartmentRoom: boolean = false;
  public showHiddenBedsLabel: boolean = false;
  public numberOfHiddenBeds: number = 0;
  public beds: Array<{ id: string, name: string }> = [];

  constructor(private _router: Router) { }

  ngOnInit(): void {
    switch (this.room.typeKey) {
      case "HOTEL":
        this.roomTypeDescription = "Hotel room";
        break;
      case "HOSTEL":
        this.roomTypeDescription = "Hostel room";
      
        break;
      case "APPARTMENT":
        this.roomTypeDescription = "Appartment";
        break;
      default:
        this.roomTypeDescription = "Unknown";
        this.roomCategory = "Temporary";
        break;
    }

    if (this.room.beds && this.room.beds.length > 0) {
      let numberOfBeds = this.room.beds.length;
      this.showBedsSection = numberOfBeds > 0;

      this.beds = this.room.beds.map(b => { return { id: b.id, name: b.name }; });
      this.numberOfHiddenBeds = 0;

      if (numberOfBeds > 7) {
        this.showHiddenBedsLabel = true;
        this.beds = this.beds.slice(0, 6);
        this.numberOfHiddenBeds = numberOfBeds - 6;
      }
    }
  }

}
