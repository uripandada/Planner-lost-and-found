import { Injectable } from '@angular/core';
import { ActivatedRouteSnapshot, Resolve } from '@angular/router';
import { Observable } from 'rxjs';
import { AutomaticHousekeepingUpdatesClient, AutomaticHousekeepingUpdateSettingsListItem, GetListOfAutomaticHousekeepingUpdateSettingsQuery } from '../autogenerated-clients/api-client';

@Injectable({ providedIn: 'root' })
export class AutomaticHousekeepingUpdatesListResolver implements Resolve<Observable<AutomaticHousekeepingUpdateSettingsListItem[]>> {
  constructor(private client: AutomaticHousekeepingUpdatesClient) { }

  resolve(routeSnapshot: ActivatedRouteSnapshot) {
    return this.client.getListOfAutomaticHousekeepingUpdateSettings(new GetListOfAutomaticHousekeepingUpdateSettingsQuery({ hotelId: null }));
  }
}
