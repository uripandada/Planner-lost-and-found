import { Injectable } from '@angular/core';
import { ActivatedRouteSnapshot, Resolve } from '@angular/router';
import { Observable } from 'rxjs';
import { CleaningPluginsClient, CleaningPluginsConfigurationData, GetCleaningPluginsConfigurationDataQuery } from '../autogenerated-clients/api-client';

@Injectable({ providedIn: 'root' })
export class CleaningPluginsConfigurationResolver implements Resolve<Observable<CleaningPluginsConfigurationData>> {
  constructor(private cleaningPluginsClient: CleaningPluginsClient) { }

  resolve(routeSnapshot: ActivatedRouteSnapshot) {
    return this.cleaningPluginsClient.getCleaningPluginsConfiguration(new GetCleaningPluginsConfigurationDataQuery({}));
  }
}
