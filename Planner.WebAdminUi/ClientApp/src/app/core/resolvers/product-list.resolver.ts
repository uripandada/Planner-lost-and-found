import { Injectable } from '@angular/core';
import { ActivatedRouteSnapshot, Resolve } from '@angular/router';
import { Observable } from 'rxjs';
import { GetListOfProductsQuery, ProductClient, ProductListItem } from '../autogenerated-clients/api-client';

@Injectable({ providedIn: 'root' })
export class ProductListResolver implements Resolve<Observable<ProductListItem[]>> {
  constructor(private _productClient: ProductClient) { }

  resolve(routeSnapshot: ActivatedRouteSnapshot) {
    return this._productClient.getListOfProducts(new GetListOfProductsQuery({}));
  }
}
